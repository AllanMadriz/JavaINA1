    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed

        LNUsuario logica = new LNUsuario();

        try {
            String user = txtCorreo.getText();
            String pass = txtPassword.getText();
            String condicion = "Correo='" + user + "' and Password='" + pass + "' and IDUsuarioRol = 2";
            logica.LoginUsuario(condicion);
            JOptionPane.showMessageDialog(null, logica.getMensaje());
            if (logica.getMensaje().equals("Logeado con exito!!")) {
                this.dispose();
                new frmMenu().setVisible(true);
            }
            Limpiar();
        } catch (Exception e) {
            try {
                throw e;
            } catch (Exception ex) {
                Logger.getLogger(frmLogin.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnIngresarActionPerformed

